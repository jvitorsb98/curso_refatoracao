package br.com.alura.adopet.api.model.enums;

/**
 * Enum que representa os tipos de pet disponíveis no sistema.
 *
 * <p>Esta enumeração define os tipos possíveis de animais, utilizados para categorizar os pets registrados no sistema Adopet.</p>
 *
 * <p>Os tipos de pet disponíveis são:</p>
 * <ul>
 *     <li><strong>GATO</strong>: Representa um gato.</li>
 *     <li><strong>CACHORRO</strong>: Representa um cachorro.</li>
 * </ul>
 */
public enum TipoPet {
    /**
     * Representa um gato.
     */
    GATO,

    /**
     * Representa um cachorro.
     */
    CACHORRO;
}
